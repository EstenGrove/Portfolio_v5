const extractShades = (selectorStr = ".color-item") => {
	const list = Array.from(document.querySelectorAll(selectorStr));
	const colorMap = list.reduce((acc, el, idx) => {
		const key = `${idx + 1}00`;
		acc[key] = el?.style?.backgroundColor;
		return acc;
	}, {});

	return colorMap;
};
